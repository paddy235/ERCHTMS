using System;
using ERCHTMS.Code;
using System.ComponentModel.DataAnnotations.Schema;
using System.ComponentModel.DataAnnotations;

namespace ERCHTMS.Entity.BaseManage
{
    /// <summary>
    /// 描 述：外包工程项目信息
    /// </summary>
    [Table("BIS_PROJECT")]
    public class ProjectEntity : BaseEntity
    {
        #region 实体成员
        /// <summary>
        /// 项目主键
        /// </summary>
        /// <returns></returns>
        [Column("PROJECTID")]
        public string ProjectId { get; set; }
        /// <summary>
        /// 创建用户主键
        /// </summary>
        /// <returns></returns>
        [Column("CREATEUSERID")]
        public string CreateUserId { get; set; }
        /// <summary>
        /// 创建时间
        /// </summary>
        /// <returns></returns>
        [Column("CREATEDATE")]
        public DateTime? CreateDate { get; set; }
        /// <summary>
        /// 创建用户
        /// </summary>
        /// <returns></returns>
        [Column("CREATEUSERNAME")]
        public string CreateUserName { get; set; }
        /// <summary>
        /// 修改用户主键
        /// </summary>
        /// <returns></returns>
        [Column("MODIFYUSERID")]
        public string ModifyUserId { get; set; }
        /// <summary>
        /// 修改时间
        /// </summary>
        /// <returns></returns>
        [Column("MODIFYDATE")]
        public DateTime? ModifyDate { get; set; }
        /// <summary>
        /// 修改用户
        /// </summary>
        /// <returns></returns>
        [Column("MODIFYUSERNAME")]
        public string ModifyUserName { get; set; }
        /// <summary>
        /// 创建用户所属部门编码
        /// </summary>
        /// <returns></returns>
        [Column("CREATEUSERDEPTCODE")]
        public string CreateUserDeptCode { get; set; }
        /// <summary>
        /// 创建用户所属机构编码
        /// </summary>
        /// <returns></returns>
        [Column("CREATEUSERORGCODE")]
        public string CreateUserOrgCode { get; set; }
        /// <summary>
        /// 项目名称
        /// </summary>
        /// <returns></returns>
        [Column("PROJECTNAME")]
        public string ProjectName { get; set; }
        /// <summary>
        /// 项目状态
        /// </summary>
        /// <returns></returns>
        [Column("PROJECTSTATUS")]
        public string ProjectStatus { get; set; }
        /// <summary>
        /// 项目开始时间
        /// </summary>
        /// <returns></returns>
        [Column("PROJECTSTARTDATE")]
        public DateTime? ProjectStartDate{ get; set; }
        /// <summary>
        /// 项目结束时间
        /// </summary>
        /// <returns></returns>
        [Column("PROJECTENDDATE")]
        public DateTime? ProjectEndDate { get; set; }
        /// <summary>
        /// 所属单位code
        /// </summary>
        /// <returns></returns>
        [Column("PROJECTDEPTCODE")]
        public string ProjectDeptCode { get; set; }
        /// <summary>
        /// 所属单位
        /// </summary>
        /// <returns></returns>
        [Column("PROJECTDEPTNAME")]
        public string ProjectDeptName { get; set; }
        /// <summary>
        /// 项目内容
        /// </summary>
        /// <returns></returns>
        [Column("PROJECTCONTENT")]
        public string ProjectContent { get; set; }

        /// <summary>
        /// 机构编码
        /// </summary>
        /// <returns></returns>
        [Column("ORGANIZECODE")]
        public string OrganizeCode { get; set; }
        #endregion

        #region 扩展操作
        /// <summary>
        /// 新增调用
        /// </summary>
        public override void Create()
        {
            this.ProjectId = Guid.NewGuid().ToString();
            this.CreateDate = DateTime.Now;
            this.CreateUserId = OperatorProvider.Provider.Current().UserId;
            this.CreateUserName = OperatorProvider.Provider.Current().UserName;
            this.CreateUserDeptCode = OperatorProvider.Provider.Current().DeptCode;
            this.CreateUserOrgCode = OperatorProvider.Provider.Current().OrganizeCode;
        }
        /// <summary>
        /// 编辑调用
        /// </summary>
        /// <param name="keyValue"></param>
        public override void Modify(string keyValue)
        {
            this.ProjectId = keyValue;
            this.ModifyDate = DateTime.Now;
            this.ModifyUserId = OperatorProvider.Provider.Current().UserId;
            this.ModifyUserName = OperatorProvider.Provider.Current().UserName;
        }
        #endregion
    }
}